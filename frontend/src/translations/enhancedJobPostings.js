/**
 * Get translation for a key
 * @param {string} lang - Language code (ar, en)
 * @param {string} key - Translation key (e.g., 'bookmark.save')
 * @param {object} params - Parameters for interpolation
 * @returns {string} Translated text
 */
export function t(lang, key, params = {}) {
  const keys = key.split('.');
  let value = enhancedJobPostingsTranslations[lang] || enhancedJobPostingsTranslations.ar;
  
  for (const k of keys) {
    value = value?.[k];
    if (!value) break;
  }
  
  if (typeof value !== 'string') {
    return key; // Return key if translation not found
  }
  
  // Replace parameters
  return value.replace(/\{(\w+)\}/g, (match, param) => {
    return params[param] !== undefined ? params[param] : match;
  });
}

/**
 * Get plural form based on count
 * @param {string} lang - Language code
 * @param {string} key - Translation key
 * @param {number} count - Count for pluralization
 * @param {object} params - Additional parameters
 * @returns {string} Translated text with correct plural form
 */
export function tp(lang, key, count, params = {}) {
  const pluralKey = count === 1 ? key : `${key}Plural`;
  return t(lang, pluralKey, { count, ...params });
}

